import {expect} from "chai";
import {pacificAtlantic} from "../src/leetcode/pacific-atlantic-water-flow";
import * as deepEqualInAnyOrder from 'deep-equal-in-any-order';
import {use} from 'chai';

use(deepEqualInAnyOrder);

describe("pacific-atlantic-water-flow", () => {
  it("1", () => {
    expect(pacificAtlantic([[1, 2, 2, 3, 5], [3, 2, 3, 4, 4], [2, 4, 5, 3, 1], [6, 7, 1, 4, 5], [5, 1, 1, 2, 4]])).deep.equalInAnyOrder([[0, 4], [1, 3], [1, 4], [2, 2], [3, 0], [3, 1], [4, 0]])
  });
  it("2", () => {
    expect(pacificAtlantic([[1, 1], [1, 1], [1, 1]])).deep.equalInAnyOrder([[0, 0], [0, 1], [1, 0], [1, 1], [2, 0], [2, 1]])
  });
  it("3", () => {
    let val = pacificAtlantic([
      [16, 19, 15, 2, 17, 7],
      [13, 5, 14, 4, 8, 7],
      [2, 11, 18, 13, 1, 16],
      [18, 15, 12, 15, 4, 11],
      [19, 14, 11, 17, 18, 13],
      [17, 16, 11, 10, 15, 14],
      [18, 17, 7, 8, 9, 11],
      [2, 9, 19, 0, 14, 12],
      [5, 18, 5, 11, 5, 1],
      [5, 7, 15, 13, 0, 15],
      [1, 14, 4, 16, 1, 9],
      [19, 17, 3, 19, 16, 10],
      [6, 3, 15, 11, 9, 5],
      [3, 4, 3, 10, 4, 7],
      [14, 16, 3, 8, 2, 10],
      [18, 5, 2, 9, 0, 7],
      [13, 7, 9, 7, 18, 13],
      [0, 15, 13, 15, 10, 16],
      [2, 12, 5, 11, 13, 15],
      [17, 16, 13, 13, 14, 19],
      [17, 14, 15, 14, 13, 0],
      [11, 12, 13, 0, 11, 0],
      [1, 3, 4, 7, 9, 8],
      [3, 5, 12, 7, 7, 15],
      [0, 14, 1, 5, 9, 13],
      [12, 14, 4, 13, 16, 13],
      [18, 18, 13, 4, 6, 9],
      [2, 0, 7, 13, 11, 7],
      [19, 18, 1, 6, 18, 0]
    ]);
    expect(val).deep.equalInAnyOrder([
      [0, 5], [0, 4], [1, 5], [1, 4], [2, 5], [4, 4], [9, 2], [19, 5], [20, 4], [20, 3], [20, 2], [19, 4], [21, 4], [22, 4], [23, 5], [24, 5], [25, 5], [25, 4], [26, 1], [26, 0], [28, 0]]
    )});});



